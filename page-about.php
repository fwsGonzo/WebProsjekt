<h2>Om meg</h2>
<a class="toggler" href="javascript:void(0)"> >> </a>
<section class="toggleAble">
	<p>
	Født 1985, som første-født, nå med tre brødre som har diverse ting de er bedre enn meg i.
	Med andre ord er jeg 25 år. Jeg trodde jeg kom til å bli en gammel hund når jeg studerte,
	men det viste seg at jeg var langt fra den eneste som studerte litt senere enn vanlig.
	</p>
	
	<p>
	Startet med instrument og musikk som 7-åring, og programmering siden jeg var 12.
	Laget hundrevis av programmer med ganske bra kvalitet selv om programmeringen var ganske dårlig.
	I 2001 (eller noe sånt), fikk jeg nett, og møtte en annen programmerer som av en eller annen
	grunn lærte meg alt han kunne og litt mer.
	</p>
	
	<p>
	Laget mine første irc bots, eller noe sånt, deretter en IRCd som tok meg et par år å få til,
	med en god del totale rewrites i mellom.
	</p>
	
	<p>
	Så laget jeg en 2D spill-engine og en
	<a href="http://ssedit.fwsnet.net" target="_blank">spritesheet editor</a>
	, som nesten ble som en slags pixelCAD.
	Nå holder jeg på med flere programmerere som er Abler i mine øyne, og vi lager en blokk-verden-ting.
	Det går veldig sakte, og forferdelig usikkert. Vanskelighets-graden er nesten urimelig. Men,
	det går.
	</p>
</section>

